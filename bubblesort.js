function bubbleSort(array) {
    if (array.length !== 0 && array.length !== 1) {
        for (let i = 0; i < array.length; i++) {
            for (let j = 0; j < array.length - 1; j++) {
                if (array[j] > array[j + 1]) {
                    // swap the two
                }
            }
        }
    }
    return array;
}
